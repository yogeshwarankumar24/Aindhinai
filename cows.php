								been wiped out. And the smell–good God! Like burnt meat! I was
								hurt across the back by the fall of the horse, and there I had
								to lie until I felt better. Just like parade it had been a
								minute before–then stumble, bang, swish!”</p>
							<p>He had hid under the dead horse for a long time, peeping
								out furtively across the common. The Cardigan men had tried a
								rush, in skirmishing order, at the pit, simply to be swept out
								of existence. Then the monster had risen to its feet and had
								begun to walk leisurely to and fro across the common among the
								few fugitives, with its headlike hood turning about exactly like
								the head of a cowled human being. A kind of arm carried a
								complicated metallic case, about which green flashes
								scintillated, and out of the funnel of this there smoked the
								Heat-Ray.</p>
							<p>In a few minutes there was, so far as the soldier could
								see, not a living thing left upon the common, and every bush and
								tree upon it that was not already a blackened skeleton was
								burning. The hussars had been on the road beyond the curvature
								of the ground, and he saw nothing of them. He heard the Martians
								rattle for a time and then become still. The giant saved Woking
								station and its cluster of houses until the last; then in a
								moment the Heat-Ray was brought to bear, and the town became a
								heap of fiery ruins. Then the Thing shut off the Heat-Ray, and
								turning its back upon the artilleryman, began to waddle away
								towards the smouldering pine woods that sheltered the second
								cylinder. As it did so a second glittering Titan built itself up
								out of the pit.</p>
							<p>The second monster followed the first, and at that the
								artilleryman began to crawl very cautiously across the hot
								heather ash towards Horsell. He managed to get alive into the
								ditch by the side of the road, and so escaped to Woking. There
								his story became ejaculatory. The place was impassable. It seems
								there were a few people alive there, frantic for the most part
								and many burned and scalded. He was turned aside by the fire,
								and hid among some almost scorching heaps of broken wall as one
								of the Martian giants returned. He saw this one pursue a man,
								catch him up in one of its steely tentacles, and knock his head
								against the trunk of a pine tree. At last, after nightfall, the
								artilleryman made a rush for it and got over the railway
								embankment.</p>
							<p>Since then he had been skulking along towards Maybury, in
